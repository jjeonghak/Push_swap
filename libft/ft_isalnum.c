#include "libft.h"

int	ft_isalnum(int c)
{
	int	result;

	result = 0;
	if (48 <= c && c <= 57)
		result = 1;
	else if ((65 <= c && c <= 90) || (97 <= c && c <= 122))
		result = 1;
	return (result);
}
